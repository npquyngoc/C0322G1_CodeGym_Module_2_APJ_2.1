package James.Course_1_Introduction_To_Java.Practice.P2_VariableUseAndDeclare;

public class VariableUseAndDeclare {

    public static void main(String[] args) {

        int i = 10;
        float f = 20.5f;
        double d = 20.5d;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";

        System.out.println("\n■ Result:\n\nI = " + i + " (Integer)\nF = " + f + " (Float)\nD = " + d + " (Double)\nB = " + b + " (Boolean)\nC = " + c + " (Char)\nS = " + s + " (String)");

    }

}